# Calculation of the area and circumference of a circle

##

# determine radius and PI #

radius = 5
PI = 3.14

# calculate area # ... program statements here ...

area = PI*radius**2

# calculate circumference # ... program statements here ...
circumference = 2*PI*radius

# display results # ... program statements here ...

print(f'The area of the circle is {area} and the circumference is {circumference} ')